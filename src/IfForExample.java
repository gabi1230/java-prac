public class IfForExample {
    public static void main(String[] args) {
        int temp = 35;

        if (temp > 30) {
            System.out.println("Халуун байна.");
        } else {
            System.out.println("Зүгээр байна.");
        }
        System.out.println("1-ээс 10 хүртэл тоо:");
        for (int i = 1; i <= 10; i++){
            System.out.println(i);
        }
    }
}
